#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*t;

	i = 0;
	t = (unsigned char *)s;
	while (i < n)
	{
		if (*t == (unsigned char)c)
			return ((void *)t);
		t++;
		i++;
	}
	return (NULL);
}
