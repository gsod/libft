#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*i;

	i = (char *)s;
	while (*i != c)
	{
		if (*i == '\0')
		{
			return (NULL);
		}
		i++;
	}
	return (i);
}
